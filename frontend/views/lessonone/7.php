            <li>理解语义化，合理地使用HTML标签来构建页面</li>
            <li>能够较为熟练地使用HTML、CSS创建一个静态页面</li>
            <li>大概了解并实践企业中对于HTML及CSS的编码规范</li>
            <li>编写复用性高、可读性、可维护性好的代码</li>
        </ul>
