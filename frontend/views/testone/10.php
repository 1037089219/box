			<div class="section">
				<p class="subheading">重要说明</p>
				<p class="remarks">有一定HTML及CSS基础的同学<br>中</p>
        <p class="message">课程多数题目的解决方案都不是唯一的，这和我们在实际工作中的情况也是一致的。因此，我们的要求不仅仅是实现设计稿的效果，更是要多去思考不同的解决方案，评估不同方案的优劣，然后使用在该场景下最优雅的方式去实现。那些最终没有被我们采纳的方案，同样也可以帮助我们学到很多知识。所以，我们列出的参考资料未必是实现需求所必须的。有的时候，实现题目的要求很简单，甚至参考资料里就有，但是背后的思考和亲手去实践却是任务最关键的一部分。在学习这些资料时，要多思考，多提问，多质疑。相信通过和小伙伴们的交流，能让你的学习事半功倍。</p>
    </div>

    <div class="section">
        <p class="subheading">任务目的</p>
        <ul>
            <li>学习如何flex进行布局，学习如何根据屏幕宽度调整布局策略。</li>
        </ul>
    </div>
    <div class="section">
        <p class="subheading">任务描述</p>
        <ul>
            <li>需要实现的效果如<a target="_blank" href="/img/test1-10.png">效果图（点击打开）</a>所示，调整浏览器宽度查看响应式效果，红色的文字是说明，不需要写在 HTML 中。</li>
        </ul>
    </div>
    <div class="section">
        <p class="subheading">任务注意事项</p>
        <ul>
            <li>只需要完成HTML，CSS代码编写，不需要写JavaScript</li>
            <li>屏幕宽度小于 640px 时，调整 Flexbox 的属性以实现第四个元素移动到最前面的效果，而不要改动第一个元素的边框颜色与高度实现效果图。</li>
            <li>思考 Flexbox 布局和网格布局的异同，以及分别适用于什么样的场景。可以搜索一下别人的结论，不过要保持思辨的态度，不可直接接受别人的观点。</li>
            <li>HTML 及 CSS 代码结构清晰、规范</li>
        </ul>
    </div>
    <div class="section">
        <p class="subheading">任务协作建议</p>
