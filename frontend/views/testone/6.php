            <li>可以各自完成任务实践，也可以按照以下分模块然后各自分工完成</li>
            <li>整体布局</li>
            <li>顶部、大图及右上角</li>
            <li>中间三段文字</li>
            <li>左下角内容</li>
            <li>右下角内容</li>
