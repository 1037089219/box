    <div class="title">
        任务七：实现常见的技术产品官网的页面架构及样式布局
    </div>

    <div class="section">
        <p class="subheading">重要说明</p>
        <p class="remarks">初学者<br>简单</p>
        <p class="message">课程多数题目的解决方案都不是唯一的，这和我们在实际工作中的情况也是一致的。因此，我们的要求不仅仅是实现设计稿的效果，更是要多去思考不同的解决方案，评估不同方案的优劣，然后使用在该场景下最优雅的方式去实现。那些最终没有被我们采纳的方案，同样也可以帮助我们学到很多知识。所以，我们列出的参考资料未必是实现需求所必须的。有的时候，实现题目的要求很简单，甚至参考资料里就有，但是背后的思考和亲手去实践却是任务最关键的一部分。在学习这些资料时，要多思考，多提问，多质疑。相信通过和小伙伴们的交流，能让你的学习事半功倍。</p>
    </div>

    <div class="section">
        <p class="subheading">任务目的</p>
        <ul>
            <li>通过实现一个常见的技术产品官网，加深对于HTML，CSS的实战能力</li>
            <li>学习掌握如何在没有标注的情况下实现设计稿到页面的精确转变</li>
        </ul>
    </div>
    <div class="section">
        <p class="subheading">任务描述</p>
        <ul>
            <li>通过HTML及CSS实现设计稿 <a target="_blank" href="/img/test1-07.psd">设计稿PSD文件（点击下载）</a>，效果如<a target="_blank" href="img/test1-07.jpg">效果图（点击打开）</a></li>
            <li>设计稿是有一定宽度的，这个宽度为页面的最小宽度，也就是说，当浏览器窗口宽度小于设计稿宽度时，允许出现横向滚动条，页面内容宽度保持不变，但是当浏览器窗口宽度大于设计稿宽度时，页面部分内容的宽度应该保持和浏览器窗口宽度一致，具体哪些部分题目不做具体指明，看看大家的判断如何。</li>
        </ul>
    </div>
    <div class="section">
        <p class="subheading">任务注意事项</p>
        <ul>
            <li>只需要完成HTML，CSS代码编写，不需要写JavaScript</li>
            <li>设计稿中的图片、文案均可自行设定</li>
            <li>在Chrome中完美实现与设计稿的各项字体、布局、内外边距等样式</li>
            <li>有能力的同学可以尝试跨浏览器的兼容性</li>
            <li>有能力的同学可以在实现一遍后尝试用less, sass或者stylus等再实现一次</li>
        </ul>
    </div>
    <div class="section">
        <p class="subheading">任务协作建议</p>
        <ul>
            <li>团队集中讨论，明确题目要求，保证队伍各自对题目要求认知一致</li>
            <li>可以各自完成任务实践，也可以按照以下分模块然后各自分工完成</li>
            <li>从顶部到下一直到关于新世界（包含）</li>
            <li>查找新世界这一部分</li>
            <li>从各城市活动（包含）到新时代（包含）</li>
            <li>剩余部分</li>
            <li>交叉互相Review其他人的代码，建议每个人至少看一个同组队友的代码</li>
            <li>相互讨论，最后合成一份组内最佳代码进行提交</li>
        </ul>
